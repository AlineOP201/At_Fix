
package br.edu.java.etb.atfix4;
    
public class Exemplo08 {
    int a = 12, b = 6;
    
    public void operadoRelac() {
        System.out.println("a > b = " + (a > b)); // a maior que b
        System.out.println("a >= b = " + (a>=b)); // a maior ou igual a b
        System.out.println("a < b = " + (a<b)); // a menor que b
        System.out.println("a <= b = " + (a<=b)); // a menor ou igual a b
        System.out.println("a == b = " + (a==b)); // a igual a b
        System.out.println("a != b = " + (a!=b)); // a diferente de b
    }
    
}
