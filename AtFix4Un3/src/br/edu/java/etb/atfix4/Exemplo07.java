
package br.edu.java.etb.atfix4;

public class Exemplo07 {
        int x = 50, y = 5;
        double j = 12.56, k = 2.32;    
    
    public void operadAritm() {
        System.out.println("x + y = " + (x + y)); // Operador soma
        System.out.println("j - k = " + (j - k)); // Operador subtração
        System.out.println("x * y = " + (x * y)); // Operador multiplicação de dois inteiros
        System.out.println("y * k = " + (y * k)); // Operador multiplicação de inteiro por decimal
        System.out.println("j / k = " + (j / k)); // Operador de divisão de dois decimais
        System.out.println("j / x = " + (j / x)); // Operador de divisão de decimal por inteiro
        System.out.println("x / y = " + (x /y)); // Operador de divisão de dois inteiros
        System.out.println("x % y = " + (x % y)); // Operador resto da divisão
    }
    
}
